package codingChallenges;

public class Print10IntegerSameLine
{
  public static void main(String[] args)
  {
    for (int i = 0; i < 100; i++)
    {
      System.out.print(i + " ");
    }
    System.out.println();
    System.out.println();
    for (int i = 0; i < 100; i++)
    {
      System.out.print((i + 1) + " ");
    }
  }
}
